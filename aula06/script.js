//OPERADORES JAVASCRIPT

// + = ADIÇÃO
// - = SUBTRAÇÃO
// / = DIVISÃO
// * = MULTIPLICAÇÃO

const resultado = 7 + 3;
console.log(resultado);

const resultadoSubtracao = 50 - 100;
console.log(resultadoSubtracao);

const resultadoDivisao = 680 / 10;
console.log(resultadoDivisao);

const resultadoMultiplicação = 8 * 10;
console.log(resultadoMultiplicação);

